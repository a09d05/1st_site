	<div class="advice1">
		<img src="./images/dog_water.png" class="dog_water" align="left">
		<span class="question">Как уберечь собаку от жары?</span>
		<span class="answer">Выгуливайте питомца только рано утром и поздно вечером. Если щенок еще не приучился ходить в туалет на улице, используйте дома пеленки. Не выгуливайте собаку в жестком наморднике, а если такая необходимость есть, используйте нейлоновый. Если вы кормите собаку готовыми кормами, можно перейти на облегченные или сократить объем порции. Важно, чтобы пес имел возможность полежать и отдышаться в прохладном месте. Некоторые собаки, чтобы охладиться в жару, готовы лезть в ванну с водой или бассейны для собак.</span>
	</div>
	<div class="advice2">
		<img src="./images/dog_play.png" class="dog_play" align="right">
		<span class="question2">Как отучить щенка грызть все<br /> подряд в доме?</span>
		<span class="answer2">У вашего питомца должно быть достаточно разных игрушек: это могут быть мячики, кольца из жесткой резины, куски каната, еще что-нибудь. Если ваш питомец начал грызть мебель, примените специальные спреи. Они сделаны на основе синтетического перца, безопасны для людей и зверей, но на вкус точно не понравятся хвостатому хулигану. Возьмите за правило убирать в недоступное для щенка место все предметы, которые могут привлечь его внимание и стать для него "игрушками".</span>
	</div>
	<div class="advice3">
		<img src="./images/dog_train.png" class="dog_train" align="left">
		<span class="question3">Собака не слушается. Что делать?</span>
		<span class="answer3">Наказания должны присутствовать при обучении и воспитании, но использовать их надо правильно. Питомец должен быть наказан, если он того заслужил, но только в момент совершения "преступления" или же сразу после него. Приучайте выполнять задания с первой команды. Если пес делает вид, что не слышит команды - сразу наказывайте. Дозируйте ласку. Нельзя чрезмерно баловать и жалеть собаку при неправильном поведении. Ласка должна быть наградой за правильное и хорошее поведение. Если вы сами начали обучение питомца и поняли, что не справляетесь, обратитесь к профессионалам.</span>
